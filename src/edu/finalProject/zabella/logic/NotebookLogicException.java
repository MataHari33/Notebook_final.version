package edu.finalProject.zabella.logic;

public class NotebookLogicException extends Exception{
    public NotebookLogicException() {
    }

    public NotebookLogicException(String message) {
        super(message);
    }

    public NotebookLogicException(String message, Throwable cause) {
        super(message, cause);
    }

    public NotebookLogicException(Throwable cause) {
        super(cause);
    }

}
